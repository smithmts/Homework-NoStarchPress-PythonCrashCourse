# Moving on from 5-3, make an if-else chain.
# If color is green, earns 5 points.  If not green, earns 10 points.
# Write one version where if block runs and one where else block runs.

alien_color = 'green'

if alien_color == 'green':
    print("Player just earned 5 points!")
else:
    print("Player just earned 10 points!")

alien_color = 'red'

if alien_color == 'green':
    print("Player just earned 5 points!")
else:
    print("Player just earned 10 points!")
