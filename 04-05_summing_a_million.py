# Make a list of the numbers from one to one million, and use min and
# max to confirm those values.
numbers = list(range(1,1000001))
min_number = min(numbers)
max_number = max(numbers)
print(f"Minimum is {min_number}, Maximum is {max_number}.")

# Use sum function to see how quickly Python adds it.
sum_number = sum(numbers)
print(f"Sum is {sum_number}")
