# Moving on from 5-4, make an if-elif-else chain.
# 5 points green, 10 points yellow, 15 points red. 
# Write one version to run each message.

alien_color = 'green'

if alien_color == 'green':
    print("Player just earned 5 points!")
elif alien_color == 'yellow':
    print("Player just earned 10 points!")
else:
    print("Player just earned 15 points!")

alien_color = 'yellow'

